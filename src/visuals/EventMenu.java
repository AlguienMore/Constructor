    private EventType determinarEvento()
    {
        switch(evento.getSelectedIndex())
        {
            case 0:
                return CLICK;
            case 1:
                return KP;
            case 2:
                return KR;
            default:
                return null;
                
        }
    }
    
    private FunctionType determinarFuncion()
    {
        switch (funcion.getSelectedIndex())
        {
            case 0:
                return ERRORALERT;
            case 1:
                return WELCOMEALERT;
            default:
                return null;
        }
    }
    

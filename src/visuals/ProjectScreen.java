    private HtmlComponent createComponent()
    {
        HtmlComponent temporal;
        
        switch(elementType.getSelectedIndex())
        {
            case 0:
                temporal= new HtmlComponent(ComponenType.OUTPUT, ComponentSubType.PARAGRAPH, initialValue.getText());
                break;
            case 1:
                temporal =  new HtmlComponent(
                            ComponenType.INPUT, 
                            ComponentSubType.BUTTON, 
                            initialValue.getText(), 
                            (int)ancho.getValue(), 
                            (int)alto.getValue() );
                break;
            case 2:
                temporal =  new HtmlComponent(
                            ComponenType.INPUT, 
                            ComponentSubType.TEXTBOX, 
                            initialValue.getText(), 
                            (int)ancho.getValue(), 
                            (int)alto.getValue() );
                break;
            default:
                temporal =  new HtmlComponent(
                            ComponenType.OUTPUT, 
                            ComponentSubType.CANVAS, 
                            initialValue.getText(), 
                            (int)ancho.getValue(), 
                            (int)alto.getValue() );
                break;
                
        }
        
        return temporal;
    }
    
    
